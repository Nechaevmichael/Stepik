# Напишите программу, которая вычисляет среднее арифметическое четырех введенных целых чисел.

# Числа вводятся в одну строку через пробел

# 🚀>P.S.: Подсказка для решения 🚀
# Sample Input 1:

# 2 3 4 5
# Sample Output 1:

# 3.5

a, b, c, d = map(int, input().split())

result = (a + b + c + d) / 4

print(result)